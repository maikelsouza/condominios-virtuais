package br.com.condominiosvirtuais.entity;

public class ObraResponsavelObra {
	
	private Integer id;
	
	private Integer idObra;
	
	private Integer idResponsavelObra;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getIdObra() {
		return idObra;
	}

	public void setIdObra(Integer idObra) {
		this.idObra = idObra;
	}

	public Integer getIdResponsavelObra() {
		return idResponsavelObra;
	}

	public void setIdResponsavelObra(Integer idResponsavelObra) {
		this.idResponsavelObra = idResponsavelObra;
	}

}
