CREATE TABLE IF NOT EXISTS ESCRITORIO_CONTABILIDADE(
	ID integer NOT NULL AUTO_INCREMENT,
	NOME varchar(100) NOT NULL,
	SITE varchar(150) NULL,
	TELEFONE_COMERCIAL bigint NULL,
	TELEFONE_CELULAR bigint NULL,
	CNPJ BIGINT NOT NULL,
	SITUACAO integer NOT NULL,
	PRIMARY KEY(ID)
);

CREATE TABLE IF NOT EXISTS CONTADOR(
	ID integer NOT NULL AUTO_INCREMENT,
	ID_ESCRITORIO_CONTABILIDADE integer NOT NULL, 
	CONSTRAINT FK_CONTADOR_ID_USUARIO_ID FOREIGN KEY (ID) REFERENCES USUARIO(ID),
	CONSTRAINT FK_CONTADOR_ID_ESCRITORIO_CONTABI_ESCRITORIO_CONTABILIDADE_ID FOREIGN KEY (ID_ESCRITORIO_CONTABILIDADE) REFERENCES ESCRITORIO_CONTABILIDADE(ID),
	PRIMARY KEY(ID)
);

